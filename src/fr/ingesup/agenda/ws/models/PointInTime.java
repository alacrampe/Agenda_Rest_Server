package fr.ingesup.agenda.ws.models;

public class PointInTime {
	private Date date;
	private Time time;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Time getTime() {
		return time;
	}
	public void setTime(Time time) {
		this.time = time;
	}
}
